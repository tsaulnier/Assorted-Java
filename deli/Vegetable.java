package deli;

/**
 * Vegetables that can be present in either a sandwich or salad
 * @author Thomas Saulnier
 */
public enum Vegetable {
	/**
	 * any variety of chopped lettuce
	 */
	lettuce,
	
	/**
	 * any variety of raw, sliced tomato
	 */
	tomato,
	
	/**
	 * any variety of raw cucumbers, not pickled
	 */
	cucumbers,
	
	/**
	 * any variety of raw, mild, sliced peppers
	 */
	peppers,
	
	/**
	 * any variety of pickled cucumbers or peppers
	 */
	pickles
}
